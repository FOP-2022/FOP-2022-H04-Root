package h04;

public interface WithNaturalCoordinates {

  /**
   * @return the natural x-Coordinate
   */
  int getX();

  /**
   * @return the natural y-Coordinate
   */
  int getY();

  /**
   * Set natural x-Coordinate
   *
   * @param x the x to set
   */
  void setX(int x);

  /**
   * Set natural y-Coordinate
   *
   * @param y the y to set
   */
  void setY(int y);
}
